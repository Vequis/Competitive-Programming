#include <iostream>
#include <cmath>

using namespace std;

int main() {
    cout << pow(2,5) << endl;
    cout << pow(2, 1/5) << endl;
    cout << sqrt(25) << endl;
    cout << abs(-2.5) << endl;
    cout << ceil(1.8) << endl;
    cout << floor(1.8) << endl;

    cout << cos(1)<< endl;
    cout << acos(0.86) << endl;

    cout << log10(20) << endl;
    return 0;
}
